// ── Private state ─────────────────────────────────────────────────────────────
static esp_lcd_panel_io_handle_t s_io_handle = NULL;
static uint8_t s_framebuf[ST7306_BUF_SIZE]; // zero-initialized = all white

// ── Helper: send command with parameters ──────────────────────────────────────
static inline void send_cmd(uint8_t cmd, const uint8_t *params, int len)
{
    esp_lcd_panel_io_tx_param(s_io_handle, cmd, params, len);
}

static inline void send_cmd_no_params(uint8_t cmd)
{
    esp_lcd_panel_io_tx_param(s_io_handle, cmd, NULL, 0);
}

// ── Pixel set (landscape 2×4 block packing) ──────────────────────────────────
void st7306_set_pixel(int x, int y, int color)
{
    if (x < 0 || x >= ST7306_WIDTH || y < 0 || y >= ST7306_HEIGHT) return;

    int inv_y   = ST7306_HEIGHT - 1 - y;
    int byte_x  = x / 2;
    int block_y = inv_y / 4;
    int index   = byte_x * (ST7306_HEIGHT / 4) + block_y;  // = byte_x * 75 + block_y
    int local_x = x % 2;
    int local_y = inv_y % 4;
    int bit     = 7 - (local_y * 2 + local_x);

    if (color) {
        s_framebuf[index] |= (1 << bit);
    } else {
        s_framebuf[index] &= ~(1 << bit);
    }
}

// ── Clear frame buffer ────────────────────────────────────────────────────────
void st7306_clear(void)
{
    memset(s_framebuf, 0, ST7306_BUF_SIZE);
}

// ── Push frame buffer to display ─────────────────────────────────────────────
// ── Frame buffer mutex (shared between calendar task and XiaoZhi display) ─────
static SemaphoreHandle_t s_fb_mutex = NULL;

SemaphoreHandle_t st7306_get_mutex(void)
{
    return s_fb_mutex;
}

// ── Push frame buffer to display ─────────────────────────────────────────────
void st7306_update_display(void)
{
    if (s_fb_mutex) xSemaphoreTake(s_fb_mutex, portMAX_DELAY);
    uint8_t col_addr[] = { 0x12, 0x2A };
    uint8_t row_addr[] = { 0x00, 0xC7 };
    send_cmd(0x2A, col_addr, sizeof(col_addr));
    send_cmd(0x2B, row_addr, sizeof(row_addr));
    esp_lcd_panel_io_tx_color(s_io_handle, 0x2C, s_framebuf, ST7306_BUF_SIZE);
    if (s_fb_mutex) xSemaphoreGive(s_fb_mutex);
}

// ── Draw a single 8×16 ASCII character ───────────────────────────────────────
static void draw_char_8x16(int x, int y, unsigned char c, int color)
{
    if (c > 0x7F) return;
    // Font table covers 0x00-0x7F (first 128 chars × 16 bytes)
    const uint8_t *glyph = &font8x16[(int)c * 16];
    for (int dy = 0; dy < 16; dy++) {
        uint8_t line = glyph[dy];
        for (int dx = 0; dx < 8; dx++) {
            if (line & (0x80 >> dx)) {
                st7306_set_pixel(x + dx, y + dy, color);
            }
        }
    }
}

